public class Prova {
    public static int somma(int a, int b){
        return (a+b);
    }
    public static int sottrai(int a, int b){
        return (a-b);
    }
    public static int moltiplica(int a, int b){
        return (a*b);
    }
    public static int dividi(int a, int b){
        return (a/b);
    }
}
